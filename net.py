import torch, torchvision
from torch import nn
import torchsummary

def get_n_params(model):
    return sum([el.numel() for el in model.parameters()])


def get_model(name, num_classes, pretrained=False, dropout=0.2):
    if name == "mobilenet":
        NET = torchvision.models.mobilenet_v2(pretrained=pretrained).to("cuda"); 
        NET.classifier = torch.nn.Sequential(torch.nn.BatchNorm1d(1280), torch.nn.Dropout(0.5), torch.nn.Linear(1280, num_classes)).to("cuda")
    elif name == "resnet18":
        NET = torchvision.models.resnet.resnet18(pretrained=pretrained).to("cuda") 
        NET.fc = torch.nn.Sequential(torch.nn.BatchNorm1d(512), torch.nn.Dropout(0.5), torch.nn.Linear(512, num_classes)).to("cuda")
    elif name == "dropoutmn":
        NET = DropoutMobileNet(num_classes=num_classes, prob_dropout=dropout).to("cuda")
    elif name == "2classifiermn":
        NET = torchvision.models.mobilenet_v2(pretrained=pretrained).to("cuda"); 
        NET.classifier = torch.nn.Sequential(torch.nn.BatchNorm1d(1280), torch.nn.Dropout(0.5), torch.nn.Linear(1280, 1280),
                                             torch.nn.BatchNorm1d(1280), torch.nn.Dropout(0.5), torch.nn.Linear(1280, num_classes)).to("cuda")
    else:
        raise ValueError("cannot find model named '{}'".format(name))
    return NET



def save_model_info(model, file):
    with open(file, mode='xt') as f:
        # the tuple (3,224,224) represents the standard size for an input image
        f.write(torchsummary.summary(model, (3, 224, 224)))
        


